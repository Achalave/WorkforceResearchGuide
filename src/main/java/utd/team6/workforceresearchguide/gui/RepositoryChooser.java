    public String showDialog() {
        this.setTitle("Select Repository Location");
        this.setModalityType(ModalityType.APPLICATION_MODAL);
        this.setVisible(true);
        if (pathField.getText().isEmpty()) {
            return null;
        } else {
            return pathField.getText();
        }
    }

