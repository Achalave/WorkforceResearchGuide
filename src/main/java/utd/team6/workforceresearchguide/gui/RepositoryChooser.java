    public RepositoryChooser(String repPath) {
        this();
        if (repPath != null) {
            File f = new File(repPath);
            fileChooser.setCurrentDirectory(f.getAbsoluteFile());
            pathField.setText(f.getAbsolutePath());
        }
    }

